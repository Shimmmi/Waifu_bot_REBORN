def upgrade() -> None:
    conn = op.get_bind()
    for (act, num), (name, desc) in DUNGEON_LORE.items():
        conn.execute(
            sa.text(
                "UPDATE dungeons SET name = :name, description = :desc "
                "WHERE act = :act AND dungeon_number = :num AND dungeon_type = 1"
            ).bindparams(name=name, desc=desc, act=act, num=num),
        )

    # Акт 4 (Мёртвые земли): добавить тег cursed к существующим тегам биома —
    # пересечение с шаблонами в коде остаётся валидным; усиливается вес undead/demon
    # (см. CURSED_TAG_WEIGHT_MULTIPLIER в services/dungeon.py).
    # Колонка tags в схеме — JSON, не JSONB; операции || и @> делаем на jsonb, затем каст в json.
    conn.execute(
        sa.text(
            """
            UPDATE dungeons
            SET tags = (COALESCE(tags::jsonb, '[]'::jsonb) || '["cursed"]'::jsonb)::json
            WHERE act = 4 AND dungeon_type = 1 AND dungeon_number BETWEEN 1 AND 5
              AND NOT (COALESCE(tags::jsonb, '[]'::jsonb) @> '["cursed"]'::jsonb)
            """
        )
    )


